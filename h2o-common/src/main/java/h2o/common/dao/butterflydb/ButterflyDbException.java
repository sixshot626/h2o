package h2o.common.dao.butterflydb;

public class ButterflyDbException extends RuntimeException {


	private static final long serialVersionUID = -3536413949990769796L;

	public ButterflyDbException() {
		super();
	}

	public ButterflyDbException(String message, Throwable cause) {
		super(message, cause);
	}

	public ButterflyDbException(String message) {
		super(message);
	}

	public ButterflyDbException(Throwable cause) {
		super(cause);
	}
	
	

}
